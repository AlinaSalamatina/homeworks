function pow(x, y) {
  let result = 1;

  for (let i = 0; i < y; i++) {
    result *= x;
  }

  return result;
}

// Приклад використання
console.log(pow(2, 4)); // Виведе нам у консолі 16
// Ця функція використовує цикл для множення числа x на себе стільки разів, скільки вказано в ступені y. Таким чином, pow(2, 4) повертає 2 * 2 * 2 * 2, тобто 16.
