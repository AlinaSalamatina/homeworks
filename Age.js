// Отримання введення від користувача через prompt
let userInput = prompt("Скільки тобі років?");

// Перевірка, чи введено число та чи воно не є від'ємним
if (!isNaN(userInput) && userInput >= 0) {
  // Переведення введеного року в число
  let years = parseInt(userInput);

  // Визначення правильного слова для виведення (рік, роки, років)
  let word;
  if (years === 1) {
    word = "рік";
  } else if (years >= 2 && years <= 4) {
    word = "роки";
  } else {
    word = "років";
  }

  // Виведення інформації
  console.log(`${years} ${word}`);
} else {
  // Виведення повідомлення про помилку, якщо введено нечислове або від'ємне значення
  console.log("Будь ласка, введіть невід'ємне число.");
}
